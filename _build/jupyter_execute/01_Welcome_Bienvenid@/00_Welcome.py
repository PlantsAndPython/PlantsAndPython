# In the second half of the course, you'll be learning about computation in bioinformatics. You'll learn how to use the command line; how to access high performance computing resources; how to store, edit, manipulate, and use biological sequences like DNA; you'll learn about genome assembly and comparative genomics; and you'll also learn about short read gene expression analysis.
