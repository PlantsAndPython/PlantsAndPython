# ## Coding basics and bioinformatics
# 
# The first half of this course covers Python and coding basics. You'll learn about variables, functions, lists, and indexing. How to visualize your data using  matplotlib. How to calculate the Golden angle with loops and model the growth of a sunflower.  And how to analyze data using pandas. Some of the examples in these lessons include plotting  out the shapes of different grapevine species, modeling the growth of sunflowers and looking for patterns in its form, and looking at the harvest dates of grapevines in Europe over centuries and how a warming planet has made their harvest dates earlier.
